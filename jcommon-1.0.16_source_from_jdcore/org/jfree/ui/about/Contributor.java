package org.jfree.ui.about;





















public class Contributor
{
  private String name;
  



















  private String email;
  



















  public Contributor(String name, String email)
  {
    this.name = name;
    this.email = email;
  }
  




  public String getName()
  {
    return name;
  }
  




  public String getEmail()
  {
    return email;
  }
}
